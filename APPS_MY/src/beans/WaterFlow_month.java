package beans;

public class WaterFlow_month {
	private int date;
	private float water;
	private int tonnage;
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	public float getWater() {
		return water;
	}
	public void setWater(float water) {
		this.water = water;
	}
	public int getTonnage() {
		return tonnage;
	}
	public void setTonnage(int tonnage) {
		this.tonnage = tonnage;
	}
	
}
