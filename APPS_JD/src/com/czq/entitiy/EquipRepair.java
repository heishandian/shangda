package com.czq.entitiy;

public class EquipRepair {
	String short_title;
	String repairreason;
	String repaircontent;
	String consumematerial;
	String repairman;
	String completetime;
	public String getShort_title() {
		return short_title;
	}
	public void setShort_title(String short_title) {
		this.short_title = short_title;
	}
	public String getRepairreason() {
		return repairreason;
	}
	public void setRepairreason(String repairreason) {
		this.repairreason = repairreason;
	}
	public String getRepaircontent() {
		return repaircontent;
	}
	public void setRepaircontent(String repaircontent) {
		this.repaircontent = repaircontent;
	}
	public String getConsumematerial() {
		return consumematerial;
	}
	public void setConsumematerial(String consumematerial) {
		this.consumematerial = consumematerial;
	}
	public String getRepairman() {
		return repairman;
	}
	public void setRepairman(String repairman) {
		this.repairman = repairman;
	}
	public String getCompletetime() {
		return completetime;
	}
	public void setCompletetime(String completetime) {
		this.completetime = completetime;
	}
	
}
